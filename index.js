// escape sequence character

let fruits = 'bana\'na'
console.log(fruits)

// string in string
// result will show bana'na
// because of \

let fruit = 'bana\nna'
console.log(fruit)

// result will be bana
// na
// na will be on second line coz of \n
// other escaping characters are \t for tab and \r for carriage return

let fruit1 = 'bana\tna'
console.log(fruit1)
